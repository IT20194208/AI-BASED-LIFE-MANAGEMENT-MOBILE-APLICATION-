package com.example.smartdiary;

public class DiaryItemMonday {
    private String text;
    private String time;


    public DiaryItemMonday(String text, String time) {
        this.text = text;
        this.time = time;

    }

    public String getText() {
        return text;
    }

    public String getTime() {
        return time;
    }


}
